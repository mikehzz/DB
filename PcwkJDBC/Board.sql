DROP INDEX PK_BOARD;

/* 게시판2 */
DROP TABLE BOARD 
	CASCADE CONSTRAINTS;

/* 게시판2 */
CREATE TABLE BOARD (
	SEQ CHAR( 40 BYTE) NOT NULL, /* 순번 */
	TITLE VARCHAR2(200) NOT NULL, /* 제목 */
	CONTENTS CLOB, /* 내용 */
	READ_CNT NUMBER(6) DEFAULT 0, /* 조회수 */
	REQ_DT TIMESTAMP DEFAULT SYSDATE, /* 등록일 */
	REG_ID VARCHAR(255), /* 등록자 */
	MOD_DT TIMESTAMP DEFAULT SYSDATE, /* 수정일 */
	MOD_ID VARCHAR2(20) /* 수정자 */
);

COMMENT ON TABLE BOARD IS '게시판2';

COMMENT ON COLUMN BOARD.SEQ IS '순번';

COMMENT ON COLUMN BOARD.TITLE IS '제목';

COMMENT ON COLUMN BOARD.CONTENTS IS '내용';

COMMENT ON COLUMN BOARD.READ_CNT IS '조회수';

COMMENT ON COLUMN BOARD.REQ_DT IS '등록일';

COMMENT ON COLUMN BOARD.REG_ID IS '등록자';

COMMENT ON COLUMN BOARD.MOD_DT IS '수정일';

COMMENT ON COLUMN BOARD.MOD_ID IS '수정자';

CREATE UNIQUE INDEX PK_BOARD
	ON BOARD (
		SEQ ASC
	);

ALTER TABLE BOARD
	ADD
		CONSTRAINT PK_BOARD
		PRIMARY KEY (
			SEQ
		);